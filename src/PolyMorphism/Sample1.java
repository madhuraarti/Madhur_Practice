package PolyMorphism;

public class Sample1 

{

	// method over loading : Declaring multiple methods with same method name but with different argument/parameter is called method overload.
	
	
	
	// addition method with 2 int parameter
	
		public void addition (int num1,int num2)  // 10 , 20
		{
			System.out.println(num1+num2);
		}
		

		public void addition (int num1,int num2,int num3)
		{
			System.out.println(num1+num2);
		}


}
