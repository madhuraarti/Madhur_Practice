package StarPattern;

public class Pattern4 
{
   public static void main(String[] args)
	
	{   
		
		    //***
        	//***  [5*4] order matrix means 5 rows and 4 coloums hence it is called multidimensional pattern
		
	      
	   
	   for( int i=1; i<=5;i++)  // outer loop ---> rows
		
	   {  
		   
		      
		   for (int j=1;j<=4;j++) // inner loop ---> coloums
	     
		   { 
		   System.out.print("*"); 
		
	      }
	    
		   System.out.println(); 
	}
	}
   }
