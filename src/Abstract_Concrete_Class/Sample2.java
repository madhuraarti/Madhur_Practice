package Abstract_Concrete_Class;

// incomplete class or abstract class

abstract public class Sample2 
{

	// complete method    
    
    public void m1()              // method declaration
    {
		System.out.println("running method m1");  // method defination
    }

    
     // complete method   
    
    public void m2()             // method declaration
    {
		System.out.println("running method m2");   // method defination
    }


   // incomplete method or abstract method
    
   abstract public void m3();   //    ; semicolon is used to complete the method body      // method declaration      

    // abstract is used for incomplete method declaration

// incomplete method or abstract method
   
   abstract public void m4();     // method defination

}
